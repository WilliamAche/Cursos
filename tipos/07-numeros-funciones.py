# Un módulo son archivos que podmeos importar a nuestro código.
import math # math es un módulo de python

# La función round() sirve para redondear un número 
print(round(1.3)) 
print(round(1.7))
print(round(1.5))

# La función abs() traera el valor absoluto de un número, es decir quitara el signo de negativo -
print(abs(-77))
print(abs(55))

# Métodos con los módulos de math

print(math.ceil(1.1)) # math.ceil() trae el número entero superior más cercano
print(math.floor(1.9999)) # math.floor() trae el número entero inferior más cercano
print(math.isnan(23)) # math.isnan() es un booleano que da True si el valor no es un número
# print(math.isnan("23"))

print(math.pow(10, 3)) # math.pow(base, exponente), permite elevar un número a la potencia de algo
print(math.sqrt(9)) # math.sqrt(), saca la raíz cuadrada del valor dado

