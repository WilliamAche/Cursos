"""
Con el \ hace que lo que viene después del mismo lo tome como parte del string
"""

# Así también se pueden hacer COMENTARIOS

# \" para colocar "" que formen parte dentro del string. Ejemplo = "Ultimate \"Python\"" 
# \' para colocar '' que formen parte dentro del string. Ejemplo = 'Ultimate \'Python\'' 
# \\ para colocar \ que formen parte dentro del string. Ejemplo = "Ultimate \\"Python\"" 
# \n hace un salto de línea. Ejemplo = "Ultimate \n\"Python\"" 

curso = "Ultimate \"Python\"" 
#Resultado = Ultimate "Python"
print(curso)
